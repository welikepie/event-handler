var elementHeight;
var transitionOut = 500;
var transitionIn = 500;
!function ($) {

  "use strict"; // jshint ;_;


 /* TOOLTIP PUBLIC CLASS DEFINITION
  * =============================== */

  var Tooltip = function (element, options) {
    this.init('tooltip', element, options)
  }

  Tooltip.prototype = {

    constructor: Tooltip

  , init: function (type, element, options) {
      var eventIn
        , eventOut

      this.type = type
      this.$element = $(element)
      this.options = this.getOptions(options)
      this.enabled = true

      if (this.options.trigger != 'manual') {
        eventIn  = this.options.trigger == 'hover' ? 'mouseenter' : 'focus'
        eventOut = this.options.trigger == 'hover' ? 'mouseleave' : 'blur'
        this.$element.on(eventIn, this.options.selector, $.proxy(this.enter, this))
        this.$element.on(eventOut, this.options.selector, $.proxy(this.leave, this))
      }

      this.options.selector ?
        (this._options = $.extend({}, this.options, { trigger: 'manual', selector: '' })) :
        this.fixTitle()
    }

  , getOptions: function (options) {
      options = $.extend({}, $.fn[this.type].defaults, options, this.$element.data())

      if (options.delay && typeof options.delay == 'number') {
        options.delay = {
          show: options.delay
        , hide: options.delay
        }
      }

      return options
    }

  , enter: function (e) {
      var self = $(e.currentTarget)[this.type](this._options).data(this.type)

      if (!self.options.delay || !self.options.delay.show) return self.show()

      clearTimeout(this.timeout)
      self.hoverState = 'in'
      this.timeout = setTimeout(function() {
        if (self.hoverState == 'in') self.show()
      }, self.options.delay.show)
    }

  , leave: function (e) {
      var self = $(e.currentTarget)[this.type](this._options).data(this.type)

      if (this.timeout) clearTimeout(this.timeout)
      if (!self.options.delay || !self.options.delay.hide) return self.hide()

      self.hoverState = 'out'
      this.timeout = setTimeout(function() {
        if (self.hoverState == 'out') self.hide()
      }, self.options.delay.hide)
    }

  , show: function () { //tooltip creation starts here.
      var $tip
        , inside
        , pos
        , actualWidth
        , actualHeight
        , placement
        , tp
        
      if (this.hasContent() && this.enabled) {
        $tip = this.tip()
        this.setContent()
	
        if (this.options.animation) {
          $tip.addClass('fade') //adds class fade, to fade tooltip in.
        }
        placement = typeof this.options.placement == 'function' ?
          this.options.placement.call(this, $tip[0], this.$element[0]) :
          this.options.placement

        inside = /in/.test(placement)
        $tip
          .remove() //removes old instances
          .css({ top: 0, left: 0, display: 'block'}); //adds new instance
          $tip.appendTo(inside ? this.$element : this.$element[0]); //appends tooltip to element

		//console.log(this.$element[0]);
        pos = this.getPosition(inside)//gets position of div
		//console.log(pos);
		console.log($tip[0].offsetHeight);
        actualWidth = $tip[0].offsetWidth ;
        actualHeight = $tip[0].offsetHeight;
        elementHeight = this.$element[0].offsetTop;
        $tip.css({ top: 0, left: 0, display: 'none', });
        console.log(actualWidth +","+actualHeight);
        console.log("pos:"+(this.$element[0].offsetHeight)*0.375);
        switch (inside ? placement.split(' ')[1] : placement) {
          case 'bottom':
            tp = {top: pos.top + pos.height, left: pos.left + pos.width / 2 - actualWidth / 2}
            break
          case 'top':
             tp = {top: this.$element[0].offsetTop - (this.$element[0].offsetHeight)*0.3 , marginTop:actualHeight*-1,left: pos.left - actualWidth/2 + pos.width/2}
            break
          case 'left':
            tp = {top: pos.top + pos.height / 2 - actualHeight / 2, left: pos.left - actualWidth}
            break
          case 'right':
            tp = {top: pos.top + pos.height / 2 - actualHeight / 2, left: pos.left + pos.width}
            break
        }
//remove opacity setting here. Just do it here. Good reasons.
var thing = document.getElementsByClassName('tooltip');
for(var i = 0; i < thing.length; i++){
thing[i].style.opacity = null;
}
        $tip
          .css(tp) //applies CSS        
          .addClass(placement)
          .slideDown(transitionIn)
           .addClass('in') //actual fade in command
      }
    }

  , isHTML: function(text) {
      // html string detection logic adapted from jQuery
      return typeof text != 'string'
        || ( text.charAt(0) === "<"
          && text.charAt( text.length - 1 ) === ">"
          && text.length >= 3
        ) || /^(?:[^<]*<[\w\W]+>[^>]*$)/.exec(text)
    }

  , setContent: function () {
      var $tip = this.tip()
        , title = this.getTitle()

      $tip.find('.tooltip-inner')[this.isHTML(title) ? 'html' : 'text'](title)
      $tip.removeClass('fade in top bottom left right')
    }

  , hide: function () {
      var that = this
        , $tip = this.tip()
//delay tip remove. Somehow.  
 $tip.removeClass('in');
      $tip.animate({
	      top: elementHeight - 200,
      },
      transitionOut,
      "linear",
      function(){

      });
          
      //if move manually, make it happen here for out.
      function removeWithAnimation() {
        var timeout = setTimeout(function () {
          $tip.off($.support.transition.end).remove()
        }, 500)
        $tip.one($.support.transition.end, function () {
          clearTimeout(timeout)
          $tip.remove()
        })
      }

      $.support.transition && this.$tip.hasClass('fade') ?
        removeWithAnimation() :
		setTimeout(function(){
  		$($tip).remove();
		}, transitionOut);
        //$tip.hide().removeWithAnimation()
        
    }

  , fixTitle: function () {
      var $e = this.$element
      if ($e.attr('title') || typeof($e.attr('data-original-title')) != 'string') {
        $e.attr('data-original-title', $e.attr('title') || '').removeAttr('title')
      }
    }

  , hasContent: function () {
      return this.getTitle()
    }

  , getPosition: function (inside) {
      return $.extend({}, (inside ? {top: 0, left: 0} : this.$element.offset()), {
        width: this.$element[0].offsetWidth
      , height: this.$element[0].offsetHeight
      })
    }

  , getTitle: function () {
      var title
        , $e = this.$element
        , o = this.options

      title = $e.attr('data-original-title')
        || (typeof o.title == 'function' ? o.title.call($e[0]) :  o.title)

      return title
    }

  , tip: function () {
      return this.$tip = this.$tip || $(this.options.template)
    }

  , validate: function () {
      if (!this.$element[0].parentNode) {
        this.hide()
        this.$element = null
        this.options = null
      }
    }

  , enable: function () {
      this.enabled = true
    }

  , disable: function () {
      this.enabled = false
    }

  , toggleEnabled: function () {
      this.enabled = !this.enabled
    }

  , toggle: function () {
      this[this.tip().hasClass('in') ? 'hide' : 'show']()
    }

  }


 /* TOOLTIP PLUGIN DEFINITION
  * ========================= */

  $.fn.tooltip = function ( option ) {
    return this.each(function () {
      var $this = $(this)
        , data = $this.data('tooltip')
        , options = typeof option == 'object' && option
      if (!data) $this.data('tooltip', (data = new Tooltip(this, options)))
      if (typeof option == 'string') data[option]()
    })
  }

  $.fn.tooltip.Constructor = Tooltip

  $.fn.tooltip.defaults = {
    animation: false
  , placement: 'top'
  , selector: false
  , template: '<div class="tooltip" id="tool"><div class="tooltip-inner-wrapper"><div class="tooltip-inner"></div></div><div class ="tooltip-arrow-outer"><div class="tooltip-arrow"><div class="arrow"></div></div></div></div>'
  , trigger: 'hover'
  , title: ''
  , delay:''
  }

}(window.jQuery);
